# Variables
#
# Las variables se usan para almacenar información que se va a leer y manipular durante la ejecución del programa.
# 
# En una variable se puede almacenar:
#
# Un número entero (p.e. 42).
# Un número decimal (p.e. 32.78)
# Un booleano (p.e. true)
# Una cadena (p.e. "Hola Mundo")
# Un arreglo (p.e.["Make", "It", "Real", "Is", "Awesome"])
# Un hash (p.e. { name: "Make it Real", status: "rocking" })
# Un símbolo (p.e. :status)
# Nada (nil). Realmente es la representación de "nada".
# Un objeto (la instancia de una clase).
# Nota: En Ruby todo es un objeto. Los números, decimales, etc. son objetos, entonces técnicamente podríamos decir que en las variables se pueden almacenar solo objetos. Pero es bueno diferenciar porque en otros lenguajes no funciona así.
